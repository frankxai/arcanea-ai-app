export const CHOSEN_AGENTS = LUMINOR_AGENTS.filter((a) => a.tier === "chosen");
export const SIXTY_FOUR_AGENTS = LUMINOR_AGENTS.filter((a) => a.tier === "sixty-four");
export const BEYOND_AGENTS = LUMINOR_AGENTS.filter((a) => a.tier === "beyond");

export function getAgentByGate(gate: string): LuminorAgent | undefined {
  return LUMINOR_AGENTS.find((a) => a.gate === gate);
}

export function getAgentById(id: string): LuminorAgent | undefined {
  return LUMINOR_AGENTS.find((a) => a.id === id);
}

export function getAgentsByElement(element: string): LuminorAgent[] {
  return LUMINOR_AGENTS.filter((a) => a.element.includes(element));
}
